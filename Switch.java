class Switch{
	public static void main(String args[]){
		int age; 
		age=3;
		
		switch (age){
		case 1:
			System.out.println("I am one");
			break;
		
		case 2:
			System.out.println("I am two");
			break;
		
		case 3:
			System.out.println("I am three");
			break;
			
		case 4:
			System.out.println("I am four");
			break;
		
		default:
			System.out.println("I am no more a kid");
		
		break;
		}
	}
}