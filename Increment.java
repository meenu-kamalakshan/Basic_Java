class Increment{
	public static void main(String args[]){
		int tuna =5;
		System.out.println(tuna);
		++tuna;
		System.out.println(tuna);
		tuna = tuna+5;
		System.out.println(tuna);
		tuna -=8;
		System.out.println(tuna);
		System.out.println(++tuna);
		System.out.println(tuna++);
		System.out.println(--tuna);
		System.out.println(tuna*=3);
	}
}